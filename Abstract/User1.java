class User1{
private int id;
private String name;
private int marks;

User1(int id,String name,int marks){
this.id = id;
this.name = name;
this.marks = marks;
}

public int getid(){
return this.id;}

public void setid(int id){
this.id = id;
}

public String getname(){
return this.name;}

public void setname(String name){
this.name =name;}

public int getmarks(){
return this.marks;}

public void setmarks(int marks){
this.marks =marks;}
}


